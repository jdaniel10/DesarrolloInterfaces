    private boolean validarFormulario(){
       String nombre = jTextFieldNombre.getText();
       if(nombre == null || "".equals(nombre)){
           JOptionPane.showMessageDialog(this, "El campo nombre no puede estar vac√≠o", "ERROR", JOptionPane.ERROR_MESSAGE);
           return false;
       }
       return true;
    }
    
